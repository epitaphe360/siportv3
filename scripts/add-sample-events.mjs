async function addEventsToDatabase() {
  console.log('üöÄ Starting to add events to database...');
  console.log(`üìÖ Adding ${sampleEvents.length} sample events`);

  try {
    // First, check if events already exist
    const { data: existingEvents, error: checkError } = await supabase
      .from('events')
      .select('id, title')
      .limit(1);

    if (checkError) {
      console.error('‚ùå Error checking existing events:', checkError);
      return;
    }

    if (existingEvents && existingEvents.length > 0) {
      console.log('‚ö†Ô∏è  Events already exist in database.');
      console.log('Existing events found:', existingEvents.map(e => e.title));

      // Ask user if they want to add more events anyway
      console.log('\nüîÑ Adding additional events...');
    }

    // Insert events
    const { data, error } = await supabase
      .from('events')
      .insert(sampleEvents)
      .select();

    if (error) {
      console.error('‚ùå Error inserting events:', error);
      return;
    }

    console.log('‚úÖ Successfully added events to database!');
    console.log(`üìä ${data?.length || 0} events inserted`);

    // Display inserted events
    if (data) {
      console.log('\nüìã Events added:');
      data.forEach((event, index) => {
        console.log(`${index + 1}. ${event.title}`);
        console.log(`   üìÖ Date: ${new Date(event.event_date).toLocaleDateString('fr-FR')}`);
        console.log(`   üè∑Ô∏è  Category: ${event.category}`);
        console.log(`   üë• Capacity: ${event.capacity}`);
        console.log(`   üåü Featured: ${event.featured ? 'Yes' : 'No'}`);
        console.log(`   üîó Virtual: ${event.virtual ? 'Yes' : 'No'}`);
        console.log('');
      });
    }

  } catch (error) {
    console.error('‚ùå Unexpected error:', error);
  }
}

